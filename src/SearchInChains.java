public class SearchInChains {
    public static void main(String[] args) {
        var chain1 = "Hello World";
        // use index of
        var index1 = chain1.indexOf("Hello");
        System.out.println("Index of 'Hello' :"+index1);

        var index2 = chain1.indexOf("World");
        System.out.println("Index of 'World' :"+index2);
    }
}
